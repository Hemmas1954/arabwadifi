export const getSmartResponse = (userMessage) => {
  const message = userMessage.toLowerCase().trim();
  
  // البحث في الأسئلة الشائعة أولاً (أولوية عالية)
  for (const [question, answer] of Object.entries(chatbotData.faq)) {
    const questionWords = question.toLowerCase().split(' ');
    const matchCount = questionWords.filter(word => message.includes(word)).length;
    
    // إذا تطابقت معظم كلمات السؤال
    if (matchCount >= Math.ceil(questionWords.length * 0.6)) {
      return answer;
    }
  }
  
  // البحث في الكلمات المفتاحية
  for (const [keyword, data] of Object.entries(chatbotData.keywordResponses)) {
    if (data.keywords.some(k => message.includes(k))) {
      return data.response;
    }
  }
  
  // البحث المرن في الأسئلة الشائعة
  for (const [question, answer] of Object.entries(chatbotData.faq)) {
    if (message.includes(question.toLowerCase().replace(/[؟?]/g, ''))) {
      return answer;
    }
  }
  
  // التحقق من الردود الإيجابية
  const positiveWords = ['شكر', 'ممتاز', 'رائع', 'جميل', 'مفيد', 'أحسنت', 'عظيم', 'ولا أروع'];
  if (positiveWords.some(word => message.includes(word))) {
    return chatbotData.positiveResponses[Math.floor(Math.random() * chatbotData.positiveResponses.length)];
  }
  
  // التحقق من كلمات الوداع
  const farewellWords = ['وداع', 'باي', 'مع السلامة', 'إلى اللقاء', 'شكراً وداعاً'];
  if (farewellWords.some(word => message.includes(word))) {
    return '👋 **وداعاً وإلى اللقاء!**\n\nكان من دواعي سروري مساعدتك اليوم! 🌟\n\n🎓 **استمر في التعلم والتطور**\n📚 **المحلل الذكي في انتظارك دائماً**\n\n**نراك قريباً! 🤗**';
  }
  
  // رد افتراضي
  return chatbotData.fallbackResponses[Math.floor(Math.random() * chatbotData.fallbackResponses.length)];
};

